# 기본 자리형
print(10, type(10))
print(32.5234e2, type(3.14))
print(3 - 4j, type(4.5 + 8.5j))
print("python", type("456"))
print(True, type(False))

# 모임 또는 컨테이너 자료형
print([3, 4, 5], type(["str", True]))
print((2, 3, 4), type(([1, 2], 4.5)))
print({"MON": "월", "TUE": "화"}, type({"AUG": 8, "SEP": 9}))
print({1, 2, 3}, type({3.4, True}))
